Alamouti code:
\begin{equation}
X(c_1, c_2) = 
\begin{bmatrix}
c_1 & c_2 \\
-c_2^* c_1^*
\end{bmatrix}

\begin{equation}
H =
\begin{bmatrix}
h_1\\
h_2
\end{bmatrix}
\end{equation}

\begin{equation}
Y = Xh + w
\end{equation}
where $w$ is a random noise vector, 
with distribution $N(0, SNR)$.
\end{equation}

